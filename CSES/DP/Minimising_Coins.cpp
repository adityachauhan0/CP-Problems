void solve() {
	int n,x; cin >> n >>x;
	vector<int> c(n); for (int &i : c) cin >> i;
	vector<int> dp(x+1, 1e9);
	dp[0]= 0;
	// coins needed to make x;
	for (int i = 1; i <= x; ++i)
	{
		// dp[i] = 1 + min(dp[i-c)
		for(int &coin : c){
			if (i < coin) continue;
			dp[i] = min(dp[i],1+ dp[i-coin]);
		}
	}

	cout << (dp[x] >= 1e9 ? -1 : dp[x]) << endl;
}

signed main() {
	cin.tie(0)->sync_with_stdio(0);  solve();
}
