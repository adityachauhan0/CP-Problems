const int M = 1e9 + 7;
void solve()
{
    ll n;
    cin >> n;

    vector<int> ways(n + 1);
    ways[0] = 1;
    for (int i = 1; i <= n; ++i)
    {
        for (int j = 1; j <= min(6, i); j++)
        {
            ways[i] = (ways[i] + ways[i - j]) % M;
        }
    }
    cout << ways[n] << endl;
}

signed main()
{
    cin.tie(0)->sync_with_stdio(0);
    solve();
}
